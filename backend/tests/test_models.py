"""
Tests for SQLAlchemy models — creation, defaults, relationships, enums.

These tests use an in-memory SQLite database so no external services are needed.
"""

import uuid

import pytest
import pytest_asyncio
from sqlalchemy import select
from sqlalchemy.ext.asyncio import AsyncSession

from app.models.base import Base
from app.models.study import Study, StudyStatus
from app.models.agent import Agent, AgentStatus, PipelineType, ParticipantIdMode, ParticipantIdentifier
from app.models.session import Session, SessionStatus, TranscriptEntry, SpeakerRole


# ── Study Model ──────────────────────────────────────────────────

class TestStudyModel:
    async def test_create_study(self, db_session: AsyncSession, make_study):
        study = make_study(title="My Research Study")
        db_session.add(study)
        await db_session.commit()

        result = await db_session.execute(
            select(Study).where(Study.id == study.id)
        )
        fetched = result.scalar_one()
        assert fetched.title == "My Research Study"
        assert fetched.status == StudyStatus.DRAFT

    async def test_study_status_enum(self):
        assert StudyStatus.DRAFT.value == "draft"
        assert StudyStatus.ACTIVE.value == "active"
        assert StudyStatus.PAUSED.value == "paused"
        assert StudyStatus.COMPLETED.value == "completed"

    async def test_study_with_description(self, db_session: AsyncSession, make_study):
        study = make_study(description="Detailed description here")
        db_session.add(study)
        await db_session.commit()

        result = await db_session.execute(
            select(Study).where(Study.id == study.id)
        )
        fetched = result.scalar_one()
        assert fetched.description == "Detailed description here"

    async def test_study_timestamps_set(self, db_session: AsyncSession, make_study):
        study = make_study()
        db_session.add(study)
        await db_session.commit()
        await db_session.refresh(study)

        # SQLite doesn't support server_default with func.now() the same way,
        # but the model should still have an id
        assert study.id is not None


