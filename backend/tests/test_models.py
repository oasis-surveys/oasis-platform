# ── Session Model ─────────────────────────────────────────────────

class TestSessionModel:
    async def test_create_session(self, db_session: AsyncSession, make_study, make_agent, make_session):
        study = make_study()
        db_session.add(study)
        await db_session.flush()

        agent = make_agent(study_id=study.id)
        db_session.add(agent)
        await db_session.flush()

        session = make_session(agent_id=agent.id)
        db_session.add(session)
        await db_session.commit()

        result = await db_session.execute(
            select(Session).where(Session.id == session.id)
        )
        fetched = result.scalar_one()
        assert fetched.status == SessionStatus.ACTIVE
        assert fetched.agent_id == agent.id

    async def test_session_status_enum(self):
        assert SessionStatus.ACTIVE.value == "active"
        assert SessionStatus.COMPLETED.value == "completed"
        assert SessionStatus.TIMED_OUT.value == "timed_out"
        assert SessionStatus.ERROR.value == "error"

    async def test_speaker_role_enum(self):
        assert SpeakerRole.USER.value == "user"
        assert SpeakerRole.AGENT.value == "agent"
        assert SpeakerRole.SYSTEM.value == "system"

    async def test_transcript_entry(self, db_session: AsyncSession, make_study, make_agent, make_session):
        study = make_study()
        db_session.add(study)
        await db_session.flush()

        agent = make_agent(study_id=study.id)
        db_session.add(agent)
        await db_session.flush()

        session = make_session(agent_id=agent.id)
        db_session.add(session)
        await db_session.flush()

        entry = TranscriptEntry(
            id=uuid.uuid4(),
            session_id=session.id,
            role=SpeakerRole.USER,
            content="Hello, this is a test.",
            sequence=1,
        )
        db_session.add(entry)
        await db_session.commit()

        result = await db_session.execute(
            select(TranscriptEntry).where(TranscriptEntry.session_id == session.id)
        )
        fetched = result.scalar_one()
        assert fetched.content == "Hello, this is a test."
        assert fetched.role == SpeakerRole.USER
        assert fetched.sequence == 1
